    private void RetrieveSnacksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RetrieveSnacksActionPerformed
        // TODO add your handling code here:
        ResultSet newrs=conn.getSnackReport();
        try{
            DefaultTableModel newmodel=(DefaultTableModel)SnacksReportJTable.getModel();
            while(newrs.next()){
             Vector<String>snackitems=new Vector<String>();
             snackitems.add(newrs.getString("customername"));
             snackitems.add(newrs.getString("purchase"));
             snackitems.add(newrs.getString("amount"));
             snackitems.add(newrs.getString("discount"));
             snackitems.add(newrs.getString("balance"));
             newmodel.addRow(snackitems);
            }
        }
        catch(SQLException x){
            JOptionPane.showMessageDialog(null, x.getMessage());
        }
    }//GEN-LAST:event_RetrieveSnacksActionPerformed

